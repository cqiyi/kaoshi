/*************************************************\
6．编一C程序，它能读入一串（n个）整数（以-9999为结束标记），并判断第1个整数在后(n-1)个整数中出现的次数，再输出该次数。（输入时，两个相邻的整数用空格隔开)。
\*************************************************/
#include "iostream.h"
void main()
{
	int i,n = 0;
	cout<<"enter nums"<<endl;
	int temp;
	cin>>i;
	temp = i;
	while ( i != -9999) {
		cin>>i;
		if(temp == i)
			n++;
	}
	cout<<"the num is"<<n<<endl;
}